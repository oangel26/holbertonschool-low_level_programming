/**
 * add - Function that adds two intergers
 * @a: interger a
 * @b: interger b
 * Return: integer sum of a and b
 */

int add(int a, int b)
{
  return (a + b);
}

/**
 * sub - Function that subtracts two intergers
 * @a: interger a
 * @b: interger b
 * Return: integer subtactrion of a and b
 */

int sub(int a, int b)
{
  return (a - b);
}

/**
 * mul - Function that multiplies two intergers
 * @a: interger a
 * @b: interger b
 * Return: integer multiplication of a and b
 */

int mul(int a, int b)
{
  return (a * b);
}

/**
 * div - Function that divides two intergers
 * @a: interger a
 * @b: interger b
 * Return: integer dividsion of a and b
 */

int div(int a, int b)
{
  return (a / b);
}

/**
 * mod - Function that return mod of two intergers
 * @a: interger a
 * @b: interger b
 * Return: integer sum of a and b
 */

int mod(int a, int b)
{
  return (a % b);
}


